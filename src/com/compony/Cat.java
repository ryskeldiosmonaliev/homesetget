package com.compony;

public class Cat {
   private String color;
   private int age;
   private String breed;
   private double whole;

   void setColor(String uzerColor) {
      color = uzerColor;
   }

   public String getColor() {
      return color;
   }
   void setAge(int uzerAge) {
      age = uzerAge;
   }

   public int getAge() {
      return age;
   }
   void setBreed(String uzerBreed) {
      breed = uzerBreed;
   }

   public String getBreed() {
      return breed;
   }
   void setWhole(double uzerWhole) {
      whole = uzerWhole;
   }

   public double getWhole() {
      return whole;
   }
}
