package com.compony;

public class Dog {
    private String name;
    private String color;
    private int age;
    private String breed;

    void setName(String uzerName) {
        name = uzerName;
    }

    public String getName() {
        return name;
    }

    void setColor(String uzerColor) {
        color = uzerColor;
    }

    public String getColor() {
        return color;
    }

    void setAge(int uzerAge) {
        age = uzerAge;
    }

    public int getAge() {
        return age;
    }

    void setBreed(String uzerBreed) {
        breed = uzerBreed;
    }

    public String getBreed() {
        return breed;
    }

 }

